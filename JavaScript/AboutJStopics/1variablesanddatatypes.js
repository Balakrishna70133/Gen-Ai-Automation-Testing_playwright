// variables and data types in JavaScript

// variable declaration and initialization 
let x = 10; // number variable
let y = "Hello"; // string variable
console.log(x); // 10
console.log(y); // Hello


// variable reassignment
let name = "Alice"; // string variable 
let age = 30; // number variable
let isStudent = false; // boolean variable
console.log(name);
console.log(age);
console.log(isStudent);


// variable naming rules
// 1. variable names can only contain letters, digits, underscores, and dollar signs
// 2. variable names must begin with a letter, underscore, or dollar sign
// 3. variable names are case-sensitive
// 4. variable names cannot be reserved keywords
// example of valid variable names
let firstName = "John";
let last_name = "Doe";
let $age = 25;
let _isStudent = true;
// example of invalid variable names
// let 1name = "John"; // cannot start with a digit
// let first-name = "John"; // cannot contain hyphens
// let var = "test"; // cannot use reserved keywords
