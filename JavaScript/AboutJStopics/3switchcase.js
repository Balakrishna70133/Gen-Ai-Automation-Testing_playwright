// Switch case problem code
let day = 8; // 1 for Monday, 2 for Tuesday, ..., 7 for Sunday
switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
        break;
    case 7:
        console.log("Sunday");
        break;
    default:
        console.log("Invalid day");
}

// Switch case problem code another example
let grade = 'A'; // A, B, C, D, F 
switch (grade) { 
    case 'A':
        console.log("Excellent");
        break;
    case 'B':
        console.log("Good");
        break;
    case 'C':
        console.log("Average");
        break;
    case 'D':
        console.log("Below Average");
        break;
    case 'F':
        console.log("Fail");
        break;
    default:
        console.log("Invalid grade");
}
